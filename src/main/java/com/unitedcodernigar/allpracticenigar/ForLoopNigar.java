package com.unitedcodernigar.allpracticenigar;

public class ForLoopNigar {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.print(i+" ");
        }
        System.out.println("************");
        for (int q = 1; q <= 34; q++)
            System.out.print(q+" ");

        System.out.println("**************");
        for (int o = 1; o <= 90; o++) {
            System.out.print(o+" ");
        }

        System.out.println("*************");

        for (int i = 20; i>= 1; i--) {
            System.out.print(i+" ");
        }
        System.out.println("**************");
        for (int i = 10; i >= 1; i--) {
            System.out.print(i+" ");
        }

        System.out.println("**************");

        for (int p = 30; p >= 0; p--) {
            System.out.print(p+" ");
        }

        System.out.println("*****************");

        for (int v = 45; v >= 35; v--) {
            System.out.print(v+" ");
        }

        System.out.println("******************");

        for (int m=15; m>=10; m--) {
            System.out.print(m+" ");
        }
        System.out.println();

        for (int i=24; i>=20; i--) {
            System.out.print(i+" ");
        }
        System.out.println();

        for (int i=0; i<=6; i++) {
            System.out.print(i+" ");
        }
        System.out.println();

        for (int t=18; t>=9; t--) {
            System.out.print(t+" ");
        }


    }
}
